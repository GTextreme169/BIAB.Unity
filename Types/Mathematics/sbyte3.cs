namespace BIAB.Mathematics
{
    /// <summary>
    /// 3 sbyte (-128 to 127)
    /// </summary>
    public struct sbyte3
    {
        public sbyte x;
        public sbyte y;
        public sbyte z;
        public sbyte3(sbyte x, sbyte y, sbyte z) : this()
        {
            this.x = x;
            this.y = y;
            this.z = z;
        }

    }
}